package com.syntax.class7;

public class DoWhileDemo {

	public static void main(String[] args) {

		int a=1;
		while (a<=3) {
			System.out.println("Hi");
			a++;
		}
		
		//we can use do while 
		System.out.println("-------CODE Using DO While loop --------");
		
		int b=1;
		do {
			System.out.println("Hello");
			b++;
		}while (b<=3);
		
		
	}

}
